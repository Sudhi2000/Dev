import { Component, Inject, OnInit } from '@angular/core';
import { FormGroup, FormBuilder, Validators } from '@angular/forms';
import { MAT_DIALOG_DATA, MatDialogRef } from '@angular/material/dialog';
import { MatSnackBarHorizontalPosition, MatSnackBarVerticalPosition, MatSnackBar } from '@angular/material/snack-bar';
import { Router } from '@angular/router';
import { EsgService } from 'src/app/services/esg.service';

@Component({
  selector: 'app-add-water-withdrawal-stress',
  templateUrl: './add-water-withdrawal-stress.component.html',
  styleUrls: ['./add-water-withdrawal-stress.component.scss']
})
export class AddWaterWithdrawalStressComponent implements OnInit {


  Form: FormGroup
  lov: any[] = []
  horizontalPosition: MatSnackBarHorizontalPosition = 'center';
  verticalPosition: MatSnackBarVerticalPosition = 'bottom';

  constructor(private router: Router,
    private formBuilder: FormBuilder,
    @Inject(MAT_DIALOG_DATA) public data: any,
    private esgService: EsgService,
    private _snackBar: MatSnackBar,
    public dialogRef: MatDialogRef<AddWaterWithdrawalStressComponent>) { }


  ngOnInit() {
    this.lov = this.data.lov
    this.Form = this.formBuilder.group({
      id: [''],
      water_source: [this.data.data.water_source || '', Validators.required],
      third_party_water: [this.data.data.third_party_water || ''],
      water_type: [this.data.data.water_type || '', Validators.required],
      quantity: [this.data.data.quantity || null, Validators.required],
      unit: [this.lov[44].value[0].Value || this.data.data.unit || '', Validators.required],
      title: [this.data.data.title || ''],
      title_ref_id: [this.data.data.reference_id || ''],
      esg_disclosure: [this.data.refID || ''],
    })
    if (this.data.mode === 'view') {
      this.Form.disable();
    }
  }

  submit() {
    if (this.data.mode === 'modify' && this.data.data.id) {
      const formData = new FormData();
      this.Form.controls['id'].setValue(this.data.data.id)
      formData.append('data', JSON.stringify(this.Form.value))
      this.esgService.updateEnvWaterwithdrawalStress(formData).subscribe({
        next: (result: any) => {
          this.dialogRef.close(this.Form.value);
        },
        error: (err: any) => {

        },
        complete: () => {
          const statusText = "Water withdrawal details modified.";
          this._snackBar.open(statusText, 'OK', {
            duration: 3000,
            horizontalPosition: this.horizontalPosition,
            verticalPosition: this.verticalPosition,
          });
        }
      })
    } else {
      this.dialogRef.close(this.Form.value);
    }
    // this.dialogRef.close(this.Form.value);
  }

}
